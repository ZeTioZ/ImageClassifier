<script setup>
import { ref, computed, onMounted } from 'vue';

const props = defineProps(['type']);

const color = computed(() => {
  if (props.type) {
    let c;
    switch (props.type) {
      case "info":  
        c = "ls-bleu-fonce";
        break;

      case "danger":
        c = "ls-rouge";
        break;

      default:
        throw new Error("Property 'type' must be either 'info' or 'danger'.");
        break;
    }
    return c;
  }
});
</script>

<template>
  <div id="alert-1" class="flex items-center p-4 mb-4 rounded-lg bg-gray-100 border" :class="`text-${color} border-${color}`" role="alert">
    <div class="ms-3 text-sm font-medium">
      A simple info alert with an <a href="#" class="font-semibold underline hover:no-underline">example link</a>. Give it a click if you like.
    </div>

    <button id="close-1" type="button" class="ms-auto -mx-1.5 -my-1.5 rounded-full p-1.5 inline-flex items-center justify-center h-8 w-8" :class="`text-${color}`" data-dismiss-target="#alert-1" aria-label="Close">
      <svg class="w-3 h-3" aria-hidden="true" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 14 14">
        <path stroke="currentColor" stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="m1 1 6 6m0 0 6 6M7 7l6-6M7 7l-6 6"/>
      </svg>
    </button>
  </div>
</template>
